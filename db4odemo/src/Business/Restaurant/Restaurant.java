package Business.Restaurant;

/**
 *
 * @author rahuludhayakumar
 */
public class Restaurant {

   
    
    private String resNo;
    private String resName;
    private String resAddress;
    private String resManager;
    private String resContact;
    
     public String getResNo() {
        return resNo;
    }

    public void setResNo(String resNo) {
        this.resNo = resNo;
    }

    public String getResName() {
        return resName;
    }

    public void setResName(String resName) {
        this.resName = resName;
    }

    public String getResAddress() {
        return resAddress;
    }

    public void setResAddress(String resAddress) {
        this.resAddress = resAddress;
    }

    public String getResManager() {
        return resManager;
    }

    public void setResManager(String resManager) {
        this.resManager = resManager;
    }

    public String getResContact() {
        return resContact;
    }

    public void setResContact(String resContact) {
        this.resContact = resContact;
    }
}
